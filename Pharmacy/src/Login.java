    private void regesterUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regesterUserBtnActionPerformed
        // TODO add your handling code here:
        regesterUserBtn.setActionCommand("open_sesame");
        String email = emailRegisterField.getText();
        String pass = passwordRegisterField.getText();
        String repass = repeatpassFieldRegisterField.getText();
        
        if (!pass.equals(repass)) {
            registertextArea.setText("passwords do not match please"
                    + " try again with proper matching pattern or register");
        }
        BeanController bean = new BeanController(email, pass);
        if (bean.validate()) {
            if (email.equals("") || pass.equals("") || repass.equals("")) {
                registertextArea.setText("please fill all fields");
                //return;
            } else {

                String cmd = evt.getActionCommand();
                if (cmd.equals("open_sesame")) {
                    db = new Database();
                    db.signup(email, repass, registertextArea);
                    passwordRegisterField.setText("");
                    repeatpassFieldRegisterField.setText("");
                }
            }
        } else {
            registertextArea.setText(bean.getMessage());
        }
    }//GEN-LAST:event_regesterUserBtnActionPerformed

