    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
        loginBtn.setActionCommand("open_sesame");
        String email = emailLoginPanelTxt.getText();

        String password = passwordLoginTfield.getText();
        if (email.equals("") || password.equals("")) {

            LogintextArea.setText("please enter pass and email");
        } else {
            BeanController bean1 = new BeanController(email, password);
            if (bean1.validate()) {
                String cmd = evt.getActionCommand();
                if (cmd.equals("open_sesame")) {
                    db = new Database();
                    int count;
                    try {
                        count = db.login(password, email, LogintextArea);
                        //emailLoginPanelTxt.setText("");
                        passwordLoginTfield.setText("");
                        if (count == 0) {
                            LogintextArea.setText("this account does not exit"
                                    + " please check your password and "
                                    + "email or register!!!!");
                        } else {
                            dispose();
                            EventQueue.invokeLater(() -> {
                                try {
                                    Pharmacy frame = new Pharmacy();
                                    frame.setVisible(true);
                                } catch (Exception e1) {
                                    LogintextArea.setText(e1.getLocalizedMessage());
                                }
                            });
                        }
                    } catch (SQLException e1) {
                        // TODO Auto-generated catch block
                        LogintextArea.setText(e1.getLocalizedMessage());
                    }
                }
            } else {
                LogintextArea.setText(bean1.getMessage());
            }
        }
    }//GEN-LAST:event_loginBtnActionPerformed

